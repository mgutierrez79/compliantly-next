// Cookie the Go API reads to localise report generation when the
// request doesn't include an explicit ?lang= parameter. Synced from
// localStorage on every language change so the user's choice persists
// across browser sessions AND is visible to the backend on the next
// PDF/Markdown request.
const LANG_COOKIE = 'compliantly.lang'

function syncLanguageCookie(language: Language) {
  if (typeof document === 'undefined') return
  const oneYearSeconds = 60 * 60 * 24 * 365
  // SameSite=Lax matches the session cookie behaviour and lets the
  // backend read the value during normal navigation; we don't need
  // cross-site delivery for the language preference.
  document.cookie = `${LANG_COOKIE}=${language}; Max-Age=${oneYearSeconds}; Path=/; SameSite=Lax`
}

// Translator now accepts an English default as a second argument
// (positional). Use it like:
//   t('connectors.title')                            // looks up the key
//   t('dashboard.welcome', 'Welcome to Attestiv')    // falls back to the
//                                                      English literal
//                                                      if the key isn't
//                                                      defined yet.
//   t('connectors.summary', { active: 3 })           // vars only
//   t('connectors.summary', 'Hello {name}', { name }) // default + vars
//
// The fallback path lets pages adopt i18n incrementally: a page can
// switch to t() calls before the corresponding entries land in
// other-language dictionaries — the user sees the English default
// instead of a ??key?? marker. The translation backlog stays visible
// because every t() call ships its source string in code.
type TranslatorVars = Record<string, string | number>
type Translator = (
  key: string,
  defaultOrVars?: string | TranslatorVars,
  vars?: TranslatorVars,
) => string

type I18nContextValue = {
  language: Language
  setLanguage: (lang: Language) => void
  t: Translator
}

